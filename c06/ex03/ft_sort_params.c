#include <unistd.h>

void	ft_putstr(char *str);
void	ft_putchar(char c);
void	ft_writestuff(int c, char **a);
int		ft_strcmp(char *s1, char *s2);

int	main(int argc, char **argv)
{
	char	*tmp;
	int		i;

	i = 1;
	if (argc > 1)
	{
		while (i < argc - 1)
		{
			if (ft_strcmp(argv[i], argv[i + 1]) > 0)
			{
				tmp = argv[i];
				argv[i] = argv[i + 1];
				argv[i + 1] = tmp;
				i = 0;
			}
			i++;
		}
	}
	ft_writestuff(argc, argv);
	return (0);
}

void	ft_writestuff(int c, char **v)
{
	int	k;

	k = 1;
	while (k < c)
	{
		ft_putstr(v[k]);
		ft_putstr("\n");
		k++;
	}
}

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0')
	{
		if (s1[i] < s2[i])
			return (-1);
		if (s1[i] > s2[i])
			return (1);
		i++;
	}
	if (s1[i] < s2[i])
		return (-1);
	if (s1[i] > s2[i])
		return (1);
	return (0);
}
